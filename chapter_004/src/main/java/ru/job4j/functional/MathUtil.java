package ru.job4j.functional;
/**
 * Math operations class
 * @author Andrei Pashchenko.
 * @version 1
 * @since 16.03.2019
 */
public class MathUtil {
    /**
     * Adding.
     * @param left Left operand
     * @param second Right operand
     * @return Result
     */
    public static double add(int left, int second) {
        return left + second;
    }
    /**
     * Division.
     * @param left Left operand
     * @param second Right operand
     * @return Result
     */
    public static double div(int left, int second) {
        return left / second;
    }
}
